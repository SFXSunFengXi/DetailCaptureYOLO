def max_merge_feature_maps(fm1, fm2):
    # 
    # print(fm1.shape)
    # print(fm2.shape)
    assert fm1.shape == fm2.shape, "Feature maps must have the same shape"

    # 
    b, c, h, w = fm1.shape

    # 
    fft_map1 = np.fft.fftshift(np.fft.fft2(fm1, axes=(-2, -1)), axes=(-2, -1))
    fft_map1 = fft_map1.detach().numpy()
    fft_map2 = np.fft.fftshift(np.fft.fft2(fm2, axes=(-2, -1)), axes=(-2, -1))
    fft_map2 = fft_map2.detach().numpy()
    # 
    combined_fft = np.maximum(fft_map1, fft_map2)

    # FFT
    combined_map = np.fft.ifft2(np.fft.ifftshift(combined_fft, axes=(-2, -1)), axes=(-2, -1)).real

    return combined_map

